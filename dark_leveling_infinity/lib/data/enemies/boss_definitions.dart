  /// Ottieni boss per rango del gate
  static List<BossData> getBossPerGate(GateRank rango) {
    return tuttiIBoss.where((b) => b.rangoMinimo == rango).toList();
  }

  /// Ottieni un boss per ID
  static BossData? getBossById(String id) {
    try {
      return tuttiIBoss.firstWhere((b) => b.id == id);
    } catch (_) {
      return null;
    }
  }

  /// Ottieni un boss casuale per rango
  static BossData? getBossCasualePerGate(GateRank rango) {
    final bossList = getBossPerGate(rango);
    if (bossList.isEmpty) return null;
    bossList.shuffle();
    return bossList.first;
  }
